    public void populateJTableWithIssueBook(String status) {
        ArrayList<Model.Issue_Book> issueBookList = issue.issuedBooksList(status);

        String[] colNames = {"Book", "Member", "Status", "Iss-Date", "Rtn-Date", "Note"};

        Object[][] rows = new Object[issueBookList.size()][colNames.length];

        for (int i = 0; i < issueBookList.size(); i++) {
            rows[i][0] = issueBookList.get(i).getBook_id();
            rows[i][1] = issueBookList.get(i).getMember_id();
            rows[i][2] = issueBookList.get(i).getStatus();
            rows[i][3] = issueBookList.get(i).getIssue_date();
            rows[i][4] = issueBookList.get(i).getReturn_date();
            rows[i][5] = issueBookList.get(i).getNote();
        }
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable_IssueBook_.setModel(model);

        int[] widths = {70, 150, 150, 150, 150, 100};
        for (int i = 0; i < widths.length; i++) {
            TableColumn column = jTable_IssueBook_.getColumnModel().getColumn(i);
            column.setPreferredWidth(widths[i]);
        }
    }

