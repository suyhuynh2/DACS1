    private void jTable_IssueBook_MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_IssueBook_MouseClicked
        int index = jTable_IssueBook_.getSelectedRow();

        int book_id = Integer.parseInt(jTable_IssueBook_.getValueAt(index, 0).toString());
        int member_id = Integer.parseInt(jTable_IssueBook_.getValueAt(index, 1).toString());

        Model.Book selectedBook;
        Model.Member selectedMember;

        try {
            selectedBook = book.getBookById(book_id);
            jSpinner_BookID.setValue(selectedBook.getId());
            jLabel_BookName_.setText(selectedBook.getName());

            selectedMember = member.getMemberById(member_id);
            jSpinner_MemberID.setValue(selectedMember.getId());
            jLabel_MemberFullName_.setText(selectedMember.getFistName() + " " + selectedMember.getLastName());

            String issue_date = jTable_IssueBook_.getValueAt(index, 3).toString();
            String return_date = jTable_IssueBook_.getValueAt(index, 4).toString();
            //String status = jTable_IssueBook_.getValueAt(index, 4).toString();
            String note = jTable_IssueBook_.getValueAt(index, 5).toString();

            Date issDate = new SimpleDateFormat("yyyy-MM-dd").parse(issue_date);
            jDateChooser_IssueDate.setDate(issDate);

            Date rtnDate = new SimpleDateFormat("yyyy-MM-dd").parse(return_date);
            jDateChooser_ReturnDate.setDate(rtnDate);

            jTextArea_Note.setText(note);

        } catch (SQLException ex) {
            Logger.getLogger(ReturnBookForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(ReturnBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable_IssueBook_MouseClicked

    private void jButton_Delete_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Delete_ActionPerformed

        int index = jTable_IssueBook_.getSelectedRow();

        try {

            int book_id = Integer.parseInt(jTable_IssueBook_.getValueAt(index, 0).toString());
            int member_id = Integer.parseInt(jTable_IssueBook_.getValueAt(index, 1).toString());

            String issued_date = jTable_IssueBook_.getValueAt(index, 3).toString();

            issue.removeFromIssue(book_id, member_id, issued_date);
            populateJTableWithIssueBook("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Select The Element You Want To Delete From The Table" + e.getMessage(), "Select Item", 2);
        }


    }//GEN-LAST:event_jButton_Delete_ActionPerformed

