    private void jButton_Lost_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Lost_ActionPerformed
        int book_id = (int) jSpinner_BookID.getValue();
        int member_id = (int) jSpinner_MemberID.getValue();
        String note = jTextArea_Note.getText();

        SimpleDateFormat dateForm = new SimpleDateFormat("yyyy-MM-dd");
        try {
            String return_date = dateForm.format(jDateChooser_ReturnDate.getDate());

            String issue_date = dateForm.format(jDateChooser_IssueDate.getDate());

            issue.updateIssue(book_id, member_id, "Lost", issue_date, return_date, note);

            int quantity = new Model.Book().getBookById(book_id).getQuantity();
            book.setQuantity_Minus_One(book_id, quantity - 1);

            jSpinner_BookID.setValue(0);
            jSpinner_MemberID.setValue(0);
            jLabel_BookName_.setText("Book Name");
            jLabel_MemberFullName_.setText("Member Full-Name");

            jDateChooser_IssueDate.setDate(null);
            jDateChooser_ReturnDate.setDate(null);
            jTextArea_Note.setText("");

        } catch (HeadlessException | NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Select An Item From The Table", "Select Item", 2);
        } catch (SQLException ex) {
            Logger.getLogger(ReturnBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        populateJTableWithIssueBook("");
    }//GEN-LAST:event_jButton_Lost_ActionPerformed

    private void jButton_Return_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Return_ActionPerformed
        int book_id = (int) jSpinner_BookID.getValue();
        int member_id = (int) jSpinner_MemberID.getValue();
        String note = jTextArea_Note.getText();

        SimpleDateFormat dateForm = new SimpleDateFormat("yyyy-MM-dd");
        try {
            String return_date = dateForm.format(jDateChooser_ReturnDate.getDate());
            Date returnDate = dateForm.parse(return_date);

            String issue_date = dateForm.format(jDateChooser_IssueDate.getDate());
            Date issueDate = dateForm.parse(issue_date);

            if (returnDate.before(issueDate)) {
                JOptionPane.showMessageDialog(null, "The Return Date Must Be After The Issue Date", "Wrong Return Date", 2);
            } else {
                issue.updateIssue(book_id, member_id, "Returned", issue_date, return_date, note);
                jSpinner_BookID.setValue(0);
                jSpinner_MemberID.setValue(0);
                jLabel_BookName_.setText("Book Name");
                jLabel_MemberFullName_.setText("Member Full-Name");

                jDateChooser_IssueDate.setDate(null);
                jDateChooser_ReturnDate.setDate(null);
                jTextArea_Note.setText("");
            }
        } catch (HeadlessException | NullPointerException | ParseException e) {
            JOptionPane.showMessageDialog(null, "Select An Item From The Table", "Select Item", 2);
        }
        populateJTableWithIssueBook("");
    }//GEN-LAST:event_jButton_Return_ActionPerformed

