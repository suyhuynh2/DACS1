    private void jButton_Add_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Add_ActionPerformed
        String fname = jTextField_FName.getText();
        String lname = jTextField_LName.getText();
        String email = jTextField_Email.getText();
        String phone = jTextField_Phone.getText();
        String username = jTextField_UserName.getText();
        String password = String.valueOf(jPasswordField_password.getPassword());
        String retype_password = String.valueOf(jPasswordField_retypepassword.getPassword());
        String userType = "User";

        if (jCheckBox_UserType.isSelected()) {
            userType = "Admin";
        }

        if (lname.trim().isEmpty() && fname.trim().isEmpty() && email.trim().isEmpty() && phone.trim().isEmpty() && username.trim().isEmpty() && password.trim().isEmpty() && retype_password.trim().isEmpty()) {
            jLabel_Empty_FName.setForeground(Color.red);
            jLabel_Empty_LName.setForeground(Color.red);
            jLabel_Empty_Password.setForeground(Color.red);
            jLabel_Empty_Retype_Password.setForeground(Color.red);
            jLabel_Empty_UserName.setForeground(Color.red);
            jLabel_Empty_Phone.setForeground(Color.red);
            jLabel_Empty_Email.setForeground(Color.red);
        } else if (lname.trim().isEmpty()) {
            jLabel_Empty_LName.setForeground(Color.red);
        } else if (fname.trim().isEmpty()) {
            jLabel_Empty_FName.setForeground(Color.red);
        } else if (username.trim().isEmpty()) {
            jLabel_Empty_UserName.setForeground(Color.red);
        } else if (password.trim().isEmpty()) {
            jLabel_Empty_Password.setForeground(Color.red);
        } else if (retype_password.trim().isEmpty()) {
            jLabel_Empty_Retype_Password.setForeground(Color.red);
        } else if (email.trim().isEmpty()) {
            jLabel_Empty_Phone.setForeground(Color.red);
        } else if (phone.trim().isEmpty()) {
            jLabel_Empty_Email.setForeground(Color.red);
        }

        if (!password.equals(retype_password)) {
            JOptionPane.showMessageDialog(null, "Retype The Correct Password", "Password error", 0);
        } else if (users.checkUserNameExists(func.enCode(username))) {
            JOptionPane.showMessageDialog(null, "This UserName Already Exists", "UserName Error", 0);
        } else {
            users.addUser(fname, lname, email, phone, username, password, userType);
        }

        populateJTableWithUsers();
    }//GEN-LAST:event_jButton_Add_ActionPerformed

