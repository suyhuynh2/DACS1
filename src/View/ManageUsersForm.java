    private void populateJTableWithUsers() {
        ArrayList<Model.Users> usersList = users.usersList();

        String[] colNames = {"ID", "F-Name", "L-Name", "Email", "Phone", "UserName", "Password", "Type"};
        Object[][] rows = new Object[usersList.size()][colNames.length];

        for (int i = 0; i < usersList.size(); i++) {
            rows[i][0] = usersList.get(i).getId();
            rows[i][1] = usersList.get(i).getFirstName();
            rows[i][2] = usersList.get(i).getLastName();
            rows[i][3] = usersList.get(i).getEmail();
            rows[i][4] = usersList.get(i).getNumberPhone();
            rows[i][5] = usersList.get(i).getUserName();
            rows[i][6] = usersList.get(i).getPassword();
            rows[i][7] = usersList.get(i).getUserType();
        }

        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable_Users.setModel(model);

        // Set column widths after setting the model
        int[] widths = {50, 100, 100, 150, 70, 100, 100, 50};
        for (int i = 0; i < widths.length; i++) {
            TableColumn column = jTable_Users.getColumnModel().getColumn(i);
            column.setPreferredWidth(widths[i]);
        }
    }

