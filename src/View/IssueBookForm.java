    private void jButton_Issue_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Issue_ActionPerformed
        int book_id = (int) jSpinner_BookID.getValue();
        int member_id = (int) jSpinner_MemberID.getValue();
        String note = jTextArea2.getText();
        SimpleDateFormat dateForm = new SimpleDateFormat("yyyy-MM-dd");
        try {
            String issue_date = dateForm.format(jDateChooser_IssueDate.getDate());
            String return_date = dateForm.format(jDateChooser_ReturnDate.getDate());

            Date issueDate = dateForm.parse(issue_date);
            Date returnDate = dateForm.parse(return_date);

            if (!book_Exist) {
                JOptionPane.showMessageDialog(null, "You Need To Check This Book", "Check If The Book Exist", 2);
            } else if (!member_Exist) {
                JOptionPane.showMessageDialog(null, "You Need To Check This Member", "Check If The Member Exist", 2);
            } else if (!issue.checkBookAvailability(book_id)) {
                JOptionPane.showMessageDialog(null, "This Book Is Not Available Right Now", "Book Not Available", 2);

            } else if (returnDate.before(issueDate)) {
                JOptionPane.showMessageDialog(null, "The Return Date Must Be After The Issue Date", "Wrong Return Date", 2);
            } else {
                issue.addIssue(book_id, member_id, "Issued", issue_date, return_date, note);
                jSpinner_BookID.setValue(0);
                jSpinner_MemberID.setValue(0);
                jLabel_BookName_.setText("Book Name");
                jLabel_MemberFullName_.setText("Member Full-Name");
                jLabel_Available.setText("YES/NO");
                jLabel_Available.setForeground(new Color(0, 51, 204));
                jDateChooser_IssueDate.setDate(null);
                jDateChooser_ReturnDate.setDate(null);
                book_Exist = false;
                member_Exist = false;
            }
        } catch (HeadlessException | NullPointerException | ParseException e) {
            JOptionPane.showMessageDialog(null, "Select Issue Date & Return Date", "Select Date", 2);
        }
    }//GEN-LAST:event_jButton_Issue_ActionPerformed

    private void jButton_SearchBook_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchBook_ActionPerformed
        int book_id = (int) jSpinner_BookID.getValue();
        try {
            Model.Book selectedBook = book.getBookById(book_id);
            if (selectedBook != null) {
                jLabel_BookName_.setText(selectedBook.getName());
                book_Exist = true;

                if (issue.checkBookAvailability(book_id)) {
                    jLabel_Available.setText("YES");
                    jLabel_Available.setForeground(Color.GREEN);
                } else {
                    jLabel_Available.setText("NO");
                    jLabel_Available.setForeground(Color.red);
                }

            } else {
                JOptionPane.showMessageDialog(null, "This book doesn't exist", "Book Not Found", 2);
                jLabel_BookName_.setText("Book Name");
                book_Exist = false;
                jLabel_Available.setText("YES/NO");
                jLabel_Available.setForeground(new Color(0, 51, 204));
            }
        } catch (SQLException ex) {
            Logger.getLogger(IssueBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_SearchBook_ActionPerformed

    private void jButton_SearchMember_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchMember_ActionPerformed
        int member_id = (int) jSpinner_MemberID.getValue();
        try {
            Model.Member selectedMember = member.getMemberById(member_id);
            if (selectedMember != null) {
                jLabel_MemberFullName_.setText(selectedMember.getFistName() + " " + selectedMember.getLastName());
                member_Exist = true;
            } else {
                JOptionPane.showMessageDialog(null, "This member doesn't exist", "Member Not Found", 2);
                jLabel_MemberFullName_.setText("Member-Full-Name");
                member_Exist = false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(IssueBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_SearchMember_ActionPerformed

